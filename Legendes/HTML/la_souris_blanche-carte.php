         <header>
            <h2>la souris blanche</h2>
         </header>
         <p> Il était une fois un roi de France, 
            <a href="#perso-louisXI" goto="autour-legende">Louis
               XI
            </a>, et son fils, 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles
               VIII
            </a>. Tous deux habitaient le lugubre château de Plessis-lès-Tours. Dans
            l’année 1483, le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a> décida de partir
            en pèlerinage à Notre-Dame-de-Cléry. Il était mourant et hanté par de vieux
            souvenirs, notamment celui de la mort de 
            <a href="#perso-jacques-darmagnac" goto="autour-legende">Jacques d’Armagnac de Nemours</a>. Ce
            dernier, ayant comploté contre son 
            <a href="#perso-louisXI" goto="autour-legende">roi</a>, le paya de sa vie, mais la sentence de 
            <a href="#perso-louisXI" goto="autour-legende">Louis XI</a> ne s’arrêta pas là. Il avait alors
            contraint les enfants à assister à l’exécution de leur père. Le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a> regrettait, depuis, ce geste mais
            gardait encore, enfermé dans une cellule du château, l’un des fils du 
            <a href="#perso-jacques-darmagnac" goto="autour-legende">duc de Nemours</a>. 
         </p>
         <p> C’est dans cet effroyable château que grandissait 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles VIII</a>, seul si ce n’est pour une souris blanche –
            
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a> – qui lui rendait
            souvent visite. Un jour, alors qu’elle était en retard, le dauphin 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> la gronda et lui demanda la
            raison de cette impolitesse. 
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a> vint alors placer ses pattes sur les mots d’un
            évangile ouvert sur la table : « Visitez les prisonniers ». 
         </p>
         <p>
            
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> se souvint alors du
            
            <a href="#perso-fils-darmagnac" goto="autour-legende">jeune d’Armagnac</a>,
            emprisonné dans une cage du cachot du château, et auquel il avait souvent pensé. Il avait bien des
            fois souhaité lui rendre visite mais la pensée que son père le découvre l’en
            avait empêché. Cependant, à présent il regrettait ce manque de courage et
            bientôt il se résolut à y aller. 
         </p>
         <p> Le soir, accompagné d’un valet qui portait nourriture et boisson, il descendit
            jusqu’au cachot. Les scrupules du gardien de prison, qui lui refusait l’accès,
            furent levés grâce à une seule phrase. « Le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a> est bien malade. » Cela ne manquait pas de rappeler aux
            serviteurs de 
            <a href="#perso-louisXI" goto="autour-legende">Louis XI</a> que celui-ci
            n’était pas éternel et qu’il valait mieux éviter la rancune de son successeur. 
         </p>
         <p> Le geôlier amena alors 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> et
            son page jusqu’à la cage du 
            <a href="#perso-fils-darmagnac" goto="autour-legende">duc de
               Nemours
            </a> – car il s’agissait bien d’une cage ! Elle était si
            étroite, si basse, qu’elle contraignait chaque mouvement de 
            <a href="#perso-fils-darmagnac" goto="autour-legende">d’Armagnac</a> qui, quoique âgé de
            dix-sept ans, était si frêle qu’il n’en faisait que douze. 
         </p>
         <p> Il fut difficile pour le 
            <a href="#perso-charlesVIII" goto="autour-legende">dauphin</a>
            d’aborder le prisonnier, mais bientôt le dialogue s’établit. Alors que le
            
            <a href="#perso-fils-darmagnac" goto="autour-legende">duc</a> demandait des nouvelles
            du monde, l’irruption de 
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a>
            bouleversa la conversation. Elle vint se lover dans les bras de 
            <a href="#perso-fils-darmagnac" goto="autour-legende">Nemours</a>, comme elle le faisait le
            matin même dans ceux de 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a>.
            La jalousie du 
            <a href="#perso-charlesVIII" goto="autour-legende">dauphin</a> s’éteint
            cependant à mesure qu’il apprit à connaître son rival, et il continua de le
            visiter, même après que 
            <a href="#perso-louisXI" goto="autour-legende">Louis XI</a>
            revint de son pèlerinage. 
         </p>
         <p> Un soir, alors que les deux jeunes gens conversaient comme à leur habitude, des
            bruits inhabituels se firent entendre. Il s’agissait là des pas de 
            <a href="#perso-louisXI" goto="autour-legende">Louis XI</a>, accompagné de son bourreau, de
            quelques serviteurs et du geôlier qui avait trahi le secret du 
            <a href="#perso-charlesVIII" goto="autour-legende">dauphin</a>. Ils désarmèrent 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> et l’emmenèrent en dehors du
            cachot. Puis le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a> murmura à son
            bourreau quelques phrases, auxquelles celui-ci répondit : « Bien monseigneur,
            dès ce soir à minuit, cela sera fait. » Nemours comprit bien ce qui avait été
            commandé par le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a>. 
         </p>
         <p> Lorsque de nouveau des bruits de pas retentirent, et que la lumière d’une
            lanterne filtrait à travers la porte, le cœur de 
            <a href="#perso-fils-darmagnac" goto="autour-legende">Nemours</a> se serra. Il déposa 
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a> au sol et lui ordonna de se
            sauver. La porte s’ouvrit finalement et le jeune 
            <a href="#perso-fils-darmagnac" goto="autour-legende">duc</a>, les yeux fermés, attendit sa
            sentence. « Vous êtes libre. » 
         </p>
         <p> Ces mots, prononcés par une voix qu’il reconnaissait bien, le laissèrent bouche
            bée. Ce n’était pas le bourreau qui était là devant lui, mais 
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a>, calme et majestueux, dans son
            nouveau rôle de roi. Il était entouré par de nobles dames et hommes, et des
            familiers – et tous s’écriaient « Vive le roi ! ». « En effet, mon père est mort
            il y a de ça une heure et me voilà ainsi orphelin et roi. Pardonnez-le,
            
            <a href="#perso-fils-darmagnac" goto="autour-legende">Nemours</a>. » 
         </p>
         <p>
            
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> ordonna que la cage fût
            détruite aussitôt. Cependant, alors que les marteaux s’acharnaient contre les
            barreaux, le fer ne cédait pas. « Sire, interrompit un vieux moine présent dans
            l’assemblée. Cette entreprise est inutile ; il est impossible de détruire la
            cage avec des outils humain. Elle est l’œuvre d’un Bohémien, un sorcier, et
            seule une baguette de fée permettra de l’abattre. » 
         </p>
         <p>
            
            <a href="#perso-charlesVIII" goto="autour-legende">Charles</a> commanda alors que tous se
            mettent à la recherche du Bohémien – et n’oublia pas de préciser que, si l’homme
            était trouvé, la reconnaissance du 
            <a href="#perso-charlesVIII" goto="autour-legende">roi</a> saurait être récompensée. Les deux amis restèrent alors
            seuls et silencieux, inquiets à l’idée que la cage ne puisse jamais plus être
            ouverte. L’horloge sonna alors une heure et la malédiction de 
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a>prit fin. Cela faisait cent ans
            exactement qu’un sorcier avait transformé la fée en souris et, enfin, elle était
            libérée de ce mauvais sort. Alors la cage s’évanouit comme si elle n’avait
            jamais existé et 
            <a href="#perso-blanchette" goto="autour-legende">Angelina</a>, dans son
            habit de fée, volait au-dessus du sol, avec à la main sa baguette. « Les
            enfants, vous me connaissiez sous le nom de 
            <a href="#perso-blanchette" goto="autour-legende">Blanchette</a>, votre sœur souris, mais on m’appelle en réalité la
            Fée des Pleurs. Vos larmes enfin se dissipent et je dois vous quitter, pour
            accomplir d’autres missions ailleurs. Adieu ! » 
         </p>
      </div>
