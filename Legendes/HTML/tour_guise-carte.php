            <h2>La Tour de Guise</h2>
         </header>
         <p>Dans le donjon du château de
            Tours était enfermé 
            <a href="#perso-charlesI-guise" goto="autour-legende">le
               jeune duc Charles Ier de Guise
            </a>, prisonnier du 
            <a href="#perso-henriIII" goto="autour-legende">roi Henri III</a>.
         </p>
         <p>Un soir d’août 1591, au milieu des sons des batraciens, un croassement plutôt
            humain se fit entendre. 
            <a href="#perso-charlesI-guise" goto="autour-legende">Le jeune
               duc
            </a> appelait et très vite une voix fluette répondit. Une jeune
            lavandière du château se tenait en face au bout du pont. Armée d’une fronde,
            elle lança un message lesté d’une pierre qui passa entre les barreaux de la
            fenêtre. 
            <a href="#perso-charlesI-guise" goto="autour-legende">Le jeune duc</a> se hâta
            de le lire. « A demain, Monseigneur ! ».
         </p>
         <p>
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles de Guise</a> songea qu’il
            serait peut-être libre le lendemain, bien que lui-même n’en soit pas sûr. Après
            tout, la tour était très haute et de nombreux gardes surveillaient les sorties
            et l’extérieur, il s‘empressa cependant de manger le papier, faisant ainsi
            disparaitre toute preuve les incriminant.
         </p>
         <p>Ressassant ainsi les risques d’une évasion, il se rendit compte que la lavandière
            avait dissimulé une longue et solide corde dans sa corbeille à linge. Périlleux,
            pensa-t-il, mais avait-il vraiment le choix ? Il devait faire confiance à la
            blanchisseuse, sinon il ne sortirait jamais de sa prison.
         </p>
         <p>Ce ne fût que le lendemain qu’il comprit le choix de cette date : le 15 août,
            jour de l’Assomption, les gardes seraient occupés à se régaler autour d’un
            banquet, l’atmosphère serait à la détente et aux jeux. Il s’agissait là de
            l’unique chance du 
            <a href="#perso-charlesI-guise" goto="autour-legende">duc de Guise</a>
            de tromper leur vigilance.
         </p>
         <p>Afin d'occuper les gardes jouant au bilboquet ou aux osselet dans la cour,
            
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles</a> leur lança un défi
            :
         </p>
         <p>« Celui qui réussira à monter le plus vite à cloche-pied l’escalier de la tour
            gagne. » annonça-t-il.
         </p>
         <p>Dans un élan de compétition ils se précipitèrent dans cette course bancale.
            Pendant ce temps, 
            <a href="#perso-charlesI-guise" goto="autour-legende">le jeune duc</a>
            courut à sa chambre, confiant à un serviteur l’importante mission de détourner
            l’attention des geôliers, en portant de fausses accusations : « Celui-ci a
            triché ! ».
         </p>
         <p>Pendant que les gardes se défendaient et punissaient le serviteur, 
            <a href="#perso-charlesI-guise" goto="autour-legende">le duc</a> avait d’ores et déjà
            solidement accroché la corde aux barreaux de sa fenêtre et entamé sa descente.
            Mais il restait un veilleur… Celui-ci apercevant le duc, arma son arquebuse et
            tira. Effrayé, 
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles</a> lâcha la
            corde et fit une chute d’environ cinq mètres. Fort heureusement, l’atterrissage
            se fit sans trop de mal, malgré quelques légères blessures.
         </p>
         <p>
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles de Guise</a> fuit et le
            temps que les gardes se rassemblent pour le poursuivre, il avait déjà rejoint
            les rives de la Loire en direction
            de Notre-Dame-la-Riche.
            La lavandière ayant tout prévu, un cheval l’y attendait. Il se hâta de le
            monter, alors qu’encore on le poursuivait. Sur le chemin, une vieille femme le
            reconnut et alerta ses poursuivants. Mais 
            <a href="#perso-charlesI-guise" goto="autour-legende">le duc de Guise</a> réussit à les semer en traversant le Cher avec l’aide d’un passeur.
         </p>
         <p>Enfin en sécurité, le duc se demanda ce qu’il allait faire désormais. Ce qu’il ne
            savait pas encore, c’est que la lavandière l’attendait, accompagnée de tous les
            hommes soutenant sa cause.
         </p>
         <p>Lorsqu’il aperçut sa jolie complice, 
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles</a> la remercia d’un chaste baiser. Puis comprenant que le
            temps lui manquait, il reprit la route suivi de deux-cents hommes et de ses
            partisans, pour rejoindre 
            <a href="#perso-charles-mayenne" goto="autour-legende">son oncle, le
               duc de Mayenne
            </a>. Il laissa derrière lui la blanchisseuse, encore
            surprise du baiser.
         </p>
         <p>Très vite, la rumeur de l’évasion de 
            <a href="#perso-charlesI-guise" goto="autour-legende">Charles Ier de Guise</a> se répandit dans la ville et les habitants se mirent à appeler
            le donjon du château par ce
            nom, encore utilisé aujourd’hui : la Tour de Guise. 
         </p>
