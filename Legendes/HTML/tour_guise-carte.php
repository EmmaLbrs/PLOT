            <h2>Contexte</h2>
            <p>En 1588, en pleine guerre de religion, le roi de
               France Henri III fait assassiner Henri de Lorraine, duc de Guise, dit le
               Balafré. Le duc de
               Mayenne, membre de la famille réussit à s’enfuir, mais Henri III fait tout de même
               arrêter
               Charles Ier, fils ainé du
               défunt duc de Guise et le reste
               de sa famille. 
            </p>
            <p>Charles Ier, prince de Joinville
               se fait ainsi enfermer dans le donjon du château de Tours. On raconte que son évasion
               fut spectaculaire.
               
            </p>
