         <header>
            <h2>La bataille de Tours</h2>
         </header>
         <p>En l’année 732, deux armées – celles de 
            <a href="#perso-charles-martel" goto="autour-legende">Charles Martel</a> et d’
            <a href="#perso-abd-al-rahman" goto="autour-legende">Abd
               al-Rahman
            </a> – se rencontrèrent devant les remparts de Tours. En effet, la ville renfermait le tombeau de 
            <a href="#perso-stmartin" goto="autour-legende">Saint Martin</a> qui regorgeait de trésors et
            fascinait 
            <a href="#perso-omeyyades" goto="autour-legende">les Sarrasins</a>. Ceux-ci avaient
            établi leur camp non loin de là et offraient un spectacle singulier aux hommes
            de 
            <a href="#perso-charles-martel" goto="autour-legende">Charles Martel</a>. Des drapeaux
            noirs et blancs flottaient au-dessus des rangs, des chameaux se reposaient entre
            les tentes orientales et les soldats – leur front orné d’un turban –
            s’entraînaient avec leur cimeterre.
         </p>
         <p>Durant six jours, nul ne fit un mouvement. Cependant, au septième jour, 
            <a href="#perso-charles-martel" goto="autour-legende">Charles</a> ordonna à ses troupes de se
            ranger en bataille. Les cavaliers d’
            <a href="#perso-abd-al-rahman" goto="autour-legende">Abd
               al-Rahman
            </a>, fulgurants, chargèrent les premiers. Mais les soldats
            de 
            <a href="#perso-charles-martel" goto="autour-legende">Charles Martel</a> résistèrent
            grâce à leur bouclier et leur armure et parvinrent à contenir l’offensive
            ennemie. Ils tendaient leur lance vers 
            <a href="#perso-omeyyades" goto="autour-legende">les
               Omeyyades
            </a> et se dressaient ainsi, comme un rempart.
         </p>
         <p>Peu à peu, la fatigue s’emparait de la cavalerie. 
            <a href="#perso-charles-martel" goto="autour-legende">Charles Martel</a>, profitant de cette
            brèche, donna le signal à ses soldats. Aussitôt, des hurlements retentirent. Des
            flammes engloutissaient le camp 
            <a href="#perso-omeyyades" goto="autour-legende">des
               Sarrasins
            </a> ; 
            <a href="#perso-aquitains" goto="autour-legende">les Aquitains</a>,
            dirigés par 
            <a href="#perso-eudes-aquitaine" goto="autour-legende">Eudes d’Aquitaine</a>,
            étaient arrivés par l’arrière de l’armée d’
            <a href="#perso-abd-al-rahman" goto="autour-legende">Abd al-Rahman</a> et avaient ainsi semé la confusion.
         </p>
         <p>Les soldats furent alors pris en étau et frappés de tous côtés. 
            <a href="#perso-abd-al-rahman" goto="autour-legende">Abd al-Rahman</a> s’écroula sur ce champ
            de bataille, abattu par 
            <a href="#perso-charles-martel" goto="autour-legende">Charles
               Martel
            </a>. 
         </p>
      </div>
