            <h2>Contexte</h2>
            <p>La bataille de Tours, plutôt connue en
               France sous le nom de bataille de Poitiers, opposa Charles
               Martel, dirigeant les Francs et
               Burgondes, et Abd al-Rahman, à la tête des Omeyyades.
            </p>
