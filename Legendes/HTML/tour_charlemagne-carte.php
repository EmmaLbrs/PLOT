            <h2>La Tour Charlemagne</h2>
         </header>
         <p>
            
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a>, la 4ème épouse du roi des
            Francs, 
            <a href="#perso-charlemagne" goto="autour-legende">Charlemagne</a>, se rendit
            dans la ville de Tours, alors que son
            époux était en pleine tournée. De cette visite, la reine s’affaiblit et tomba
            bientôt malade. Sa santé était en danger et le roi revint de son voyage pour
            être auprès de sa bien-aimée. Le roi fit chercher un remède pour guérir la
            reine. Il pensa aux reliques de 
            <a href="#perso-stmartin" goto="autour-legende">Saint-Martin</a> qui pouvaient, d’après la légende guérir les
            maladies. On fit également appel à des savants, des moines, des botanistes, et
            même des Égyptiennes, mais rien n’avait pu encore aider son épouse.
         </p>
         <p>La reine était en train de mourir d’un mal inconnu. Entourée de ses femmes
            fidèles, elle luttait contre la mort. Son combat était vain, et la reine, petit
            à petit imagina la mort. La faucheuse prenait des traits de monstres qui
            encerclaient son cou ou d’un oiseau guettant sa proie. Quand vint le moment tant
            redouté, des prêtres vinrent réciter les Saintes Paroles à la mourante.
            
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a> la tendre épouse du
            roi, rendit son dernier souffle. Quand s’allumèrent les cierges et devant son
            corps maintenant vide de toute vie, le roi s’agenouilla.
         </p>
         <p>
            <a href="#perso-charlemagne" goto="autour-legende">Charlemagne</a> qui avait connu des
            femmes, des batailles et des couronnes n’était plus qu’un homme qui se lamentait
            d’avoir perdu l’être aimé. Il baisa les mains de sa tendre épouse et après un
            dernier regard pour elle, il se retira.
         </p>
         <p>Le jour de ses funérailles ce 4 Juin 800, la reine 
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a> était étendue à visage
            découvert, habillée de ses plus beaux habits. Son lit mortuaire, porté par les
            principaux officiers du palais, était précédé par un diacre tenant en main une
            grande croix d’argent. Ce cortège funéraire passait dans la foule sous les
            bruits de lamentation du peuple. Le roi marchait seul, la tête haute, robuste de
            corps. Il portait des habits brodés d’or et de pierres précieuses, épée près du
            corps. À son côté, un clerc portait sur un coussin brillant un diadème
            étincelant d’or et de pierreries. Le diadème de la reine. Ce cortège se
            poursuivait avec les fils de 
            <a href="#perso-charlemagne" goto="autour-legende">Charlemagne</a>. La reine 
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a> et le roi n’eurent jamais d’enfants et sa descendance
            venait de ses premières épouses. On pouvait admirer dans ce cortège des curés,
            chanceliers, comtes et domestiques du palais, sénéchaux, clercs, évêques, abbés,
            seigneurs, prêtres puis enfin des savants. Ce cortège était là pour rendre
            hommage à la reine et l’accompagner dans sa dernière demeure.
         </p>
         <p>
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a> fut enterrée dans ce
            sanctuaire, la basilique
            Saint-Martin au centre de la ville de Tours. C’est là où
            
            <a href="#perso-charlemagne" goto="autour-legende">Charlemagne</a> déposa sa dernière
            épouse, celle qu’il ne remplaça jamais. L’édifice dans lequel reposait la 4ème
            reine du roi des Francs subit un incendie et il ne resta bientôt que des
            cendres. De ces cendres naquit quelques années après une tour.
         </p>
         <p>De cette tour de pierre, qui gardait les cendres de 
            <a href="#perso-luitgarde" goto="autour-legende">Luitgarde</a> demeure depuis la Tour Charlemagne. 
         </p>
