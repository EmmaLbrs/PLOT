            <h2>Contexte</h2>
            <p>La tour Charlemagne Notre légende se déroule en 800, Charlemagne était alors roi
               des Francs et des Lombards. Il ne devint empereur d’Occident que le 25 décembre
               800, soit quelques mois après le décès de Luitgarde sa quatrième et dernière
               épouse. La campagne énoncée dans la légende fait référence au fait que
               Charlemagne faisait une tournée en Neustrie (région au nord-ouest du royaume des
               Francs).
            </p>
