            <h2>Contexte</h2>
            <p> Hugues ou Hugon Ier, comte de Tours au IXème s, avait été chargé par Charles le
               Chauve de fortifier la ville de Tours des invasions normandes (vikings). Ce
               château fort est aujourd’hui disparu. C’est au XVI s qu’on attribua le nom de
               Huguenot aux protestants qui étaient mal vus par la population catholique. Il
               était là pour critiquer et effrayer les personnes qui voudraient les rejoindre.
               
            </p>
