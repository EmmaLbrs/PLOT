            <h2>Le roi Hugon</h2>
         </header>
         <p> Au Moyen Age, dans la ville de Tours 
            un grand château fort dominait 
            les bords de Loire. Ce château avait pour vocation 
            de protéger la ville des invasions normandes. 
            <a href="#perso-roi-hugon" goto="autour-legende">Hugues Ier</a> 
            devint comte de Tours et devait par l’ordre du 
            <a href="#perso-charles-le-chauve" goto="autour-legende">roi</a>
            fortifier la ville. Il fit construire une tour carrée à laquelle il donna
            bientôt son nom. 
            <a href="#perso-roi-hugon" goto="autour-legende">Hugues Ier</a> décéda sur le champ 
            de bataille en protégeant la ville d’Orléans. 
            
         </p>
         <p>
            Quelques années après, de ce château il ne resta plus rien. 
            De ces ruines qui étaient impressionnante et sinistre on entendit des plaintes.
            Puis une apparition, la nuit. Un fantôme qui chevauchait le soir aux environs
            des villes et dans les rues et s’attaquait aux personnes qu’il rencontrait. Une
            fois son escapade finie il retournait se réfugier dans l’ancienne Tour du 
            
            <a href="#perso-roi-hugon" goto="autour-legende">roi Hugon</a>.
            C’est de cette tour bientôt surnommée la tour de feu 
            <a href="#perso-roi-hugon" goto="autour-legende">Hugon</a> 
            que l’on entendait des plaintes.
            
         </p>
         <p>
            Le souverain décédé était alors de retour et n’était pas
            prêt de s’arrêter. La population prit bientôt peur de ce revenant et des
            
            <a href="#perso-chretiens" goto="autour-legende">chrétiens</a> installèrent une cloche dans la tour pour empêcher le fantôme de
            terroriser la ville. Mais ceci ne fonctionna pas. Feu le 
            <a href="#perso-roi-hugon" goto="autour-legende">roi Hugon</a>
            prit un malin plaisir à faire sonner la cloche dès qu’il s’attaquait à quelqu’un,
            pour
            prévenir la population qu’il était là. La cloche censée faire partir le revenant
            était devenue la cloche des agonisants. Mais s’il n’y avait pas de victimes, le
            fantôme se contentait de renverser les croix du cimetière pour marquer sa
            présence. 
            
         </p>
         <p>
            On se servit de ce revenant comme mise en garde auprès des enfants
            désobéissant. Il était devenu un personnage de la ville, effrayant chacun mais
            permettant aux petits enfants d’obéir à leurs parents.
            
         </p>
         <p>
            Quelques siècles plus
            tard quand vint l’émergence du 
            <a href="#perso-protestants" goto="autour-legende">protestantisme</a>,
            les partisans de ce nouvel ordre se réunirent dans les décombres de la tour. 
            Les 
            <a href="#perso-chretiens" goto="autour-legende">catholiques</a>surnommèrent les
            
            <a href="#perso-protestants" goto="autour-legende">protestants</a> d’Huguenots en mémoire de ce fantôme qui hantait les nuits de la
            ville et du mal qu’il faisait. Un terme négatif fait pour dégouter la population
            de se joindre à cet ordre. Mais les 
            <a href="#perso-protestants" goto="autour-legende">protestants</a> gardèrent ce nom d’Huguenot et
            se l’approprièrent pour désigner chaque protestant du pays.
            
         </p>
         <p>
            C’est donc d’un fantôme qui hantait la ville de Tours
            et effrayait les petits enfants que naquit le nom d’un mouvement qui les séduits eux
            même, par 
            la signification de ce nouveau patronyme. 
         </p>
