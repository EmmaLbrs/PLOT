         <p>Alors qu’il tentait de réconcilier les
            moines Tourangeaux et les moines
            Poitevins, 
            <a href="#perso-stmartin" goto="autour-legende">Saint-Martin</a>
            mourut en 397 à Candes. Les deux
            partis mirent leurs différends de côté pour veiller ensemble sur sa
            dépouille. Mais la trêve ne dura pas. Les uns et les
            autres se disputaient les reliques de 
            <a href="#perso-stmartin" goto="autour-legende">ce grand homme, vénéré par tous.</a>
         </p>
         <p>Au milieu de la nuit, alors que les
            Poitevins dormaient profondément, les Tourangeaux décidèrent de passer
            à l’action. Il parvinrent à sortir le corps du 
            <a href="#perso-stmartin" goto="autour-legende">saint homme</a> par la fenêtre de la chapelle et l’apportèrent
            précautionneusement vers la Loire, où
            les attendait une barque. La voile levée, les hommes ramèrent, et le vent soufflant comme pour leur donner
            raison, accéléra le voyage.
         </p>
         <p>
            <a href="#perso-stmartin" goto="autour-legende">Le saint</a> se fit donc enterrer dans un
            cimetière public à Tours. Puis, en
            435, 
            <a href="#perso-stbrice" goto="autour-legende">l’évêque Saint-Brice</a> fit
            construire une chapelle au-dessus de la tombe afin de lui rendre hommage.
         </p>
         <p>Mais l’importance de 
            <a href="#perso-stmartin" goto="autour-legende">Saint-Martin</a>
            conduit à une plus grande décision encore. 
            <a href="#perso-stperpet" goto="autour-legende">Saint
               Perpet
            </a> décida en 471 de faire édifier une collégiale rappelant à
            la mémoire du 
            <a href="#perso-stmartin" goto="autour-legende">saint homme</a>. Sitôt les
            travaux terminés, il fut décidé que la consécration du bâtiment, ainsi que le
            déplacement des reliques se feraient le 1er juillet.
         </p>
         <p>Le jour même, évêques, abbés et autres fidèles se pressent au cimetière où se
            trouve encore le sarcophage dans lequel repose la dépouille de 
            <a href="#perso-stmartin" goto="autour-legende">Saint-Martin</a>. Très vite, on creuse et
            dégage la terre afin d’atteindre le sarcophage, mais alors même qu’on tente de
            le sortir de terre, celui-ci ne bouge pas d’un pouce. Après de maintes essais,
            la foule, abattue décide de rentrer. Il est décidé qu’on réessaiera demain.
         </p>
         <p>Mais le lendemain, même affaire : impossible de déplacer le sarcophage. Alors que
            les fidèles commencent à s’inquièter de ce mystère, un vieillard à la longue
            chevelure blanche s’avance au milieu de la foule désespérée. Peut-être,
            annonce-t-il, faudra-t-il encore patienter quelques jours. Après tout, le 4
            juillet se trouve être le jour de la consécration épiscopale"L'ordination épiscopale, dite encore consécration ou sacre est la
            cérémonique lithurgique au cours de laquelle le sacrement de l'ordre dans sa
            plénitude est conféré aux nouveaux évêques.", Chapeau André. Les ordinations
            épiscopales dans l'Église catholique du XVIe siècle à nos jours. In: Revue
            d'histoire de l'Église de France, tome 76, n°196, 1990. pp. 73-84..
            Ne serait-ce pas là un message de 
            <a href="#perso-stmartin" goto="autour-legende">Saint-Martin</a> ?
         </p>
         <p>Tous décident de suivre le conseil de l’étrange vieillard et d’attendre. Le 4
            juillet donc, on tente à nouveau de sortir le sarcophage de terre, et cette
            fois-ci, ô miracle, ce dernier est extrait sans aucune difficulté !
         </p>
         <p>La foule en liesse accompagne le déplacement du saint homme jusqu’à la
            collégiale, et lorsqu’on se retourne pour remercier le vieil homme de sa
            précieuse remarque, ce dernier s’est évaporé... 
         </p>
