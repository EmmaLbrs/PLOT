            <h2>Le couvent des Carmes</h2>
         </header>
         <p> Le 
            <a href="#perso-Louis-XI" goto="autour-legende">roi Louis XI </a> était en balade au
            bord du canal entre la Loire et le Cher à Tours. Il admirait ce beau canal qu’il avait fait construire
            pour que les Tourangeaux les plus fortunés puissent jouir d’un nouveau moyen de
            transport. Il continua à admirer le paysage de son pays, se posant des
            questions, cherchant comment améliorer la Touraine et la rendre plus importante
            encore qu’elle ne le fût.
            <a href="#perso-Louis-XI" goto="autour-legende"> Louis XI </a>
            était un roi qui aimait conquérir des territoires, s’agrandir toujours plus,
            pour plus de puissance. « - J’aimerais conquérir la Brenne, c’est une petite
            rivière peut-être, mais elle serait utile. » dit le 
            <a href="#perso-Louis-XI" goto="autour-legende">roi</a>. 
         </p>
         <p> Durant ces contemplations le 
            <a href="#perso-Louis-XI" goto="autour-legende">roi</a>
            entendit bientôt des bruits de pas. Il se demanda qui pouvait venir le déranger
            durant sa balade et qui pouvait donc venir si près du domaine royal. Bientôt, il
            vit apparaître un 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a>
            accompagné d’un âne. C’était un 
            <a href="#perso-moine-queteur" goto="autour-legende">mendiant
               carme
            </a>, un des 
            <a href="#perso-moine" goto="autour-legende">moines</a> de
            cet ordre catholique. Mais ce 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a> passa devant le roi sans le saluer. Il ne le regarda que
            de travers, pressant son âne d’aller plus vite. Il voulait alors fuir l’étranger
            devant lequel il passait. «- Hé ! Là-bas, le 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a>, écoute-moi. » Le 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a> s’arrêta pour écouter ce que
            voulait ce drôle d’homme. « - Je veux rentrer, donne-moi donc ton âne pour que
            je puisse aller plus vite, ordonna le roi - Cet âne est le mien, et, je ne le
            laisserai pas sur l’honneur de notre bon roi saint Louis se défendit le
            
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a>. - Et moi par
            Saint-Martin, patron des ânes, je le prendrai ! - Seul le roi pourrait me donner
            cet ordre, car c’est le roi qui décide ici. - Donc mon cher 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a>, tu respectes le roi ? - Oui,
            bien sûr, c’est notre maître, il nous a défendus quand il le fallait, nous a
            donnés certains privilèges. Et il aime même les pauvres. - Donc les 
            <a href="#perso-moine" goto="autour-legende">moines</a> carmes comme toi respectent le roi ? -
            Oui nous sommes pour le roi ! - Très bien, 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a>, tu peux partir. Je dirai quelques mots au roi sur toi et
            tes frères. » 
         </p>
         <p> Le 
            <a href="#perso-moine-queteur" goto="autour-legende">moine</a> repartit en se
            demandant qui pouvait bien être ce drôle d’homme qui lui demandait son âne et
            après changeait d’avis. 
         </p>
         <p> Dans le couvent de
            Sainte-Anne, près de Tours, les 
            <a href="#perso-moine" goto="autour-legende">carmes</a>
            étaient en pleine prière. Ils étaient très pieux et ne demandaient qu’un peu de
            pain. Malheureusement pour le couvent, ils étaient à court de farine. Leur âne,
            qui transportait les vivres, était revenu sans rien. Chacun décida de se séparer
            pour quérir un peu d’argent, de nourriture ou bien encore prier. Quand tout d’un
            coup le frère portier vint prévenir ses frères. « - Des cavaliers, des cavaliers
            ! Ils sont devant notre maison et viennent ici au nom du 
            <a href="#perso-louisXI" goto="autour-legende">roi</a>. Ils disent qu’ils sont là sous un ordre
            royal qui nous concerne, ils enfonceront la porte si nous ne leur ouvrons pas !
            - Mes frères, mes frères, agenouillons-nous et prions. Notre dernière heure est
            venue. 
            <a href="#perso-tristan" goto="autour-legende">Tristan</a> le prévôt est là avec
            ses bourreaux et peut-être que bientôt nous devrons comparaître devant le
            tribunal de Dieu, après que son armée nous ait brulés, noyés ou encore pendus. -
            Mais qu’avons-nous fait de mal ? » 
         </p>
         <p> Les 
            <a href="#perso-moine" goto="autour-legende">moines</a> étaient effrayés par la
            situation, chacun se demandait ce qu’ils avaient bien pu faire pour mériter une
            conclusion comme celle-là. D’autres se préparaient à fuir pour échapper à la
            sentence. Quand 
            <a href="#perso-tristan" goto="autour-legende">Tristan</a> apparut dans
            le couvent avec un air sévère. « - Voilà, dit-il, les ordres du 
            <a href="#perso-louisxi" goto="autour-legende">roi</a>… Vous allez… - Seigneur ! Pitié, pitié !
            dirent les 
            <a href="#perso-moine" goto="autour-legende">moines</a> agenouillés.
            Laissez-nous une journée pour que nous puissions faire pénitence. - Laissez-moi
            vendre d’abord mon âne, dit l’un des 
            <a href="#perso-moine" goto="autour-legende">moines</a>. - Priez, suppliez, criez,sanglotez, clama 
            <a href="#perso-tristan" goto="autour-legende">Tristan</a>, d’un air heureux, le 
            <a href="#perso-louisxi" goto="autour-legende">roi</a> m’envoie pour… » 
         </p>
         <p>
            
            <a href="#perso-tristan" goto="autour-legende">Tristan</a> se mit alors à rire fortement.
            Les 
            <a href="#perso-moine" goto="autour-legende">moines</a> prirent de plus en plus peur
            au fil des rires de leur bourreau. « - Le 
            <a href="#perso-louisXI" goto="autour-legende">roi</a> m’envoie vous dire qu’il vous donne le poids de l’âne en
            argent. Ce même âne appartenant au 
            <a href="#perso-moine-queteur" goto="autour-legende">moine
               quêteur
            </a> qui seulement au 
            <a href="#perso-louisxi" goto="autour-legende">roi</a> aurait donné sa bête. Pour ma part, j’aurais pris plaisir à
            le faire pendre, mais le 
            <a href="#perso-louisxi" goto="autour-legende">roi</a> veut
            qu’il vive avec ses frères. Priez en paix et avec l’argent, construisez un autre
            monastère ici même à Tours. C’est le désir du 
            <a href="#perso-louisxi" goto="autour-legende">roi</a>. » 
         </p>
         <p> Les 
            <a href="#perso-moine" goto="autour-legende">moines</a>, surpris, n’en croyant pas
            leurs yeux ni leurs oreilles, félicitèrent leur 
            <a href="#perso-moine-queteur" goto="autour-legende">frère quêteur</a> qui s’en voulut de ne
            pas avoir reconnu son souverain. Le couvent obéit à 
            <a href="#perso-louisXI" goto="autour-legende">Louis XI</a> et fit construire un nouveau
            monastère dans le centre de Tours : Le
            monastère des Carmes de Tours. 
         </p>
