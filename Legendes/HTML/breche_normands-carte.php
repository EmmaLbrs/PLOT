            <h2>Contexte</h2>
            <p>Au IVᵉ siècle, alors que la ville de Tours prospérait, elle s’entoura d’une muraille
               afin de se protéger
               d'éventuelles attaques des peuples
               germaniques. Ces remparts, symboles de richesse, contenaient l’actuel quartier
               de la Cathédrale
               Saint-Gatien, ainsi que l’amphithéâtre transformé en place forte. On peut aujourd’hui
               encore voir les traces de ces fortifications de part et d’autres de ce
               quartier.
            </p>
